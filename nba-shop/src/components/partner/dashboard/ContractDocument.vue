                                เข้าดูและบันทึกไฟล์ภาพพร้อมเสียงตลอดเวลา และตลอดระยะเวลาของสัญญาฉบับนี้ใช้บังคับ
                                ค่าใช้จ่ายที่เกิดขึ้นจากการติดกล้องวงจรทางผู้บริหารศูนย์ฯ สาขานั้นๆ เป็นผู้รับผิดชอบ
                            </p>
