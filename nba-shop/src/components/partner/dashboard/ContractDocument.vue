                                โดยตรงขึ้นตรงต่อการบริหารงานทุกระบบจากสำนักงานใหญ่ ในการจำหน่ายสินค้าและบริการทุกประเภท
                                ภายใต้
                                แบรนด์ เอ็นบีเอ ดิจิตอล เวิลด์ (NBA DIGITAL WORLDS) ในรูปแบบการลงทุนรายละเอียดดังนี้
