                                ค่าสินค้าสูญหายและความเสียหายที่เกิดขึ้นเหตุเกิดจากการยกเลิกสัญญา สามารถนำข้อ 24 มาถือ
                                ปฏิบัติ
                            </p>
                            <p style="text-indent: 1.5cm">2.4) ผู้บริหารศูนย์ สาขานั้นๆ ต้องติดกล้องวงจรปิด ตามที่บริษัท
                                เอ็นบีเอ ฯ กำหนดเท่านั้น
